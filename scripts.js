//Add Function
function add(firstValue, secondValue) {
    return (firstValue + secondValue)
}

//Subtract Function
function subtract(firstValue, secondValue) {
    return (firstValue - secondValue)
}

//Multiply Function
function multiply(firstValue, secondValue) {
    return (firstValue * secondValue)
}

//Divide Function
function divide(firstValue, secondValue) {
    return (firstValue / secondValue)
}